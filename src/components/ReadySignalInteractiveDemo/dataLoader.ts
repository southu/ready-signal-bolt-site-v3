export function formatColumnName(name: string): string {
  // Format column names for display
  if (name === 'unitSales') return 'Unit Sales';
  if (name === 'pricePerUnit') return 'Price per Unit';
  if (name === 'marketingSpend') return 'Marketing Spend';
  
  return name
    .replace(/_Lag(\d+)$/, ' (Lag $1)')
    .replace(/-/g, ' ')
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

export function getExternalColumnDisplayNames(): { key: string; display: string }[] {
  return EXTERNAL_COLUMNS.map(col => ({
    key: col,
    display: formatColumnName(col),
  }));
}
