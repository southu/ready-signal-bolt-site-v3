// ============ PARSING FUNCTIONS ============

function parseCSV(csv: string): DataSet {
  const lines = csv.trim().split('\n');
  const headers = lines[0].split(',').map(h => h.trim());
  
  const rows: DataRow[] = [];
  
  for (let i = 1; i < lines.length; i++) {
    const values = lines[i].split(',');
    if (values.length < 4 || !values[0]) continue; // Skip empty rows
    
    const row: DataRow = {
      date: formatDate(values[0]),
      unitSales: parseFloat(values[1]) || 0,
      pricePerUnit: parseFloat(values[2]) || 0,
      marketingSpend: parseFloat(values[3]) || 0,
    };
    
    // Add any additional columns (external signals)
    for (let j = 4; j < headers.length; j++) {
      if (values[j] && values[j].trim()) {
        row[headers[j]] = parseFloat(values[j]) || 0;
      }
    }
    
    rows.push(row);
  }
  
  return {
    rows,
    columns: headers,
    rowCount: rows.length,
    dateRange: {
      start: rows[0]?.date || '',
      end: rows[rows.length - 1]?.date || '',
    },
  };
}

function formatDate(dateStr: string): string {
  // Convert M/D/YY to readable format
  const parts = dateStr.split('/');
  if (parts.length !== 3) return dateStr;
  
  const month = parseInt(parts[0]);
  const year = parseInt(parts[2]) + 2000;
  const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];
  
  return `${months[month - 1]} ${year}`;
}

