export const getFeatureBySlug = (slug: string): DataFeature | undefined => {
  return dataFeatures.find(feature => feature.slug === slug);
};

export const getAllSlugs = (): string[] => {
  return dataFeatures.map(feature => feature.slug);
};

export const getFeaturesBySource = (sourceId: string): DataFeature[] => {
  return dataFeatures.filter(feature => feature.sourceId === sourceId);
};

export const getFeaturesByCategory = (category: string): DataFeature[] => {
  return dataFeatures.filter(feature => feature.category === category);
};

export const searchFeatures = (query: string): DataFeature[] => {
  const lowerQuery = query.toLowerCase();
  return dataFeatures.filter(feature => 
    feature.name.toLowerCase().includes(lowerQuery) ||
    feature.category.toLowerCase().includes(lowerQuery) ||
    feature.description.toLowerCase().includes(lowerQuery) ||
    feature.seoKeywords.some(kw => kw.toLowerCase().includes(lowerQuery))
  );
};

